export class Employee {
  id: string;
  name: string;
  designation: string;
  salary: string;
  email: string;

 constructor(id: string, name: string, designation: string, salary: string, email:string) {
  this.id = id;
  this.name = name;
  this.designation = designation ;
  this.salary = salary;
  this.email = email ;

}
}